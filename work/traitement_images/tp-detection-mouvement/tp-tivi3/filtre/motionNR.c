void FrameDifference_FirstStep(uint8 **I0, uint8 **I1, int i0, int i1, int j0, int j1, uint8 **D, uint8 **S)
// ---------------------------------------------------------------------------------------------------------
{
    int i, j;
    uint8 x0;
    
    for(i=i0; i<=i1; i++) {
        for(j=j0; j<=j1; j++) {
            
            x0 = I0[i][j];
            
            I1[i][j] =x0;
            D[i][j]  = 0;
            S[i][j]  = 0;
        }
    }
}
